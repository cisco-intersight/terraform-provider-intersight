			if err := d.Set("idle_time_expiration", (s.IdleTimeExpiration).String()); err != nil {
				return err
			}
			if err := d.Set("last_login_client", (s.LastLoginClient)); err != nil {
				return err
			}

			if err := d.Set("last_login_time", (s.LastLoginTime).String()); err != nil {
				return err
			}
